import UIKit
import ABToolKit

class FGColor: NSObject {
   
    class func blueColor() -> UIColor {
    
        return UIColor(hex: "054189")
    }
    
    class func yellowColor() -> UIColor {
        
        return UIColor(hex: "FFE054")
    }
}